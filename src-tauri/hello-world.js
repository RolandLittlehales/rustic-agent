// Hello World in JavaScript
console.log("Hello, World!");

// Alternative ways to display Hello World

// 1. Using a function
function sayHello() {
    console.log("Hello, World from a function!");
}

// 2. Using an arrow function
const helloArrow = () => {
    console.log("Hello, World from an arrow function!");
};

// 3. Using a variable
const message = "Hello, World from a variable!";
console.log(message);

// Call the functions
sayHello();
helloArrow();

